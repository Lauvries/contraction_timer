import { writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const url = String(process.env.SUPABASE_URL ?? "").trim();
const key = String(process.env.SUPABASE_ANON_KEY ?? "").trim();

const body = `// Generated by npm run build (env: SUPABASE_URL, SUPABASE_ANON_KEY). Not committed with secrets from CI.
window.SUPABASE_URL = ${JSON.stringify(url)};
window.SUPABASE_ANON_KEY = ${JSON.stringify(key)};
`;

writeFileSync(join(root, "supabase-config.js"), body, "utf8");
